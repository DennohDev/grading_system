</head>
<body>
    <div class="container mt-5">
        <h2 class="text-center mb-4">Admin Registration</h2>
        <?php if (isset($error)): ?>
            <div class="alert alert-danger text-center"><?php echo $error; ?></div>
        <?php endif; ?>
        <div class="row justify-content-center">
            <div class="col-md-6">
                <form method="POST" action="">
